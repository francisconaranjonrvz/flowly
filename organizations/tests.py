User = get_user_model()


class OrganizationModelTests(TestCase):
    def test_slug_autogenerated_and_deduped(self):
        a = Organization.objects.create(name='Acme Inc')
        b = Organization.objects.create(name='Acme Inc')
        self.assertEqual(a.slug, 'acme-inc')
        self.assertTrue(b.slug.startswith('acme-inc'))
        self.assertNotEqual(a.slug, b.slug)

    def test_membership_unique_per_user_and_org(self):
        org = Organization.objects.create(name='Acme')
        user = User.objects.create_user('u1', password='pass12345')
        OrganizationMembership.objects.create(organization=org, user=user, role='admin')
        with self.assertRaises(IntegrityError):
            with transaction.atomic():
                OrganizationMembership.objects.create(organization=org, user=user, role='member')

    def test_theme_rejects_invalid_hex(self):
        org = Organization.objects.create(name='Acme')
        theme = OrganizationTheme(organization=org, brand_accent='nothex')
        with self.assertRaises(ValidationError):
            theme.full_clean()

    def test_theme_accepts_valid_hex(self):
        org = Organization.objects.create(name='Acme')
        theme = OrganizationTheme(organization=org, brand_accent='#d6ff00')
        theme.full_clean()  # no debe lanzar


class PersonalOrgServiceTests(TestCase):
    def test_creates_personal_org_with_owner_membership(self):
        user = User.objects.create_user('u1', password='pass12345')
        org = get_or_create_personal_organization(user)
        self.assertTrue(org.is_personal)
        membership = OrganizationMembership.objects.get(user=user)
        self.assertEqual(membership.organization, org)
        self.assertEqual(membership.role, 'owner')

    def test_idempotent(self):
        user = User.objects.create_user('u1', password='pass12345')
        first = get_or_create_personal_organization(user)
        second = get_or_create_personal_organization(user)
        self.assertEqual(first.pk, second.pk)
        self.assertEqual(OrganizationMembership.objects.filter(user=user).count(), 1)


class OnAccentLuminanceTests(TestCase):
    def test_auto_chooses_dark_or_light_by_luminance(self):
        from flowly.context_processors import _hex_to_channels, _resolve_on_accent
        _, lime = _hex_to_channels('#d6ff00')
        _, indigo = _hex_to_channels('#6366F1')
        self.assertEqual(_resolve_on_accent('auto', lime), '17 24 39')      # lime -> texto oscuro
        self.assertEqual(_resolve_on_accent('auto', indigo), '255 255 255')  # indigo -> texto claro
        self.assertEqual(_resolve_on_accent('light', None), '255 255 255')
        self.assertEqual(_resolve_on_accent('dark', None), '17 24 39')


class OrgThemeInjectionTests(TestCase):
    @classmethod
    def setUpTestData(cls):
        cls.user = User.objects.create_user('lima', password='limapass1234')
        cls.org = Organization.objects.create(name='NoSoloWebs', slug='nosolowebs')
        OrganizationTheme.objects.create(
            organization=cls.org, brand_accent='#d6ff00',
            on_accent_mode='auto', font_display='ppgoshasans', theme_mode='dark',
        )
        OrganizationMembership.objects.create(organization=cls.org, user=cls.user, role='admin')

    def test_active_org_emits_brand_override(self):
        self.client.login(username='lima', password='limapass1234')
        html = self.client.get(reverse('home')).content.decode()
        self.assertIn('id="org-theme"', html)
        self.assertIn('--brand-accent:214 255 0;', html)
        self.assertIn('--brand-on-accent:17 24 39;', html)  # lime -> texto negro
        self.assertIn('PPGoshaSans', html)

    def test_user_without_theme_emits_no_override(self):
        plain = User.objects.create_user('plain', password='plainpass1234')
        org = Organization.objects.create(name='Plain Co')
        OrganizationMembership.objects.create(organization=org, user=plain, role='admin')
        self.client.login(username='plain', password='plainpass1234')
        html = self.client.get(reverse('home')).content.decode()
        self.assertNotIn('id="org-theme"', html)


class OrgSwitchTests(TestCase):
    @classmethod
    def setUpTestData(cls):
        cls.user = User.objects.create_user('sam', password='sampass1234')
        cls.org_a = Organization.objects.create(name='Org A')
        cls.org_b = Organization.objects.create(name='Org B')
        cls.foreign = Organization.objects.create(name='Foreign Co')
        OrganizationMembership.objects.create(organization=cls.org_a, user=cls.user, role='admin')
        OrganizationMembership.objects.create(organization=cls.org_b, user=cls.user, role='member')

    def test_switch_to_member_org(self):
        self.client.login(username='sam', password='sampass1234')
        self.client.post(reverse('org-switch'), {'organization': self.org_b.id})
        self.assertEqual(self.client.session['active_org_id'], self.org_b.id)

    def test_switch_to_non_member_is_rejected(self):
        self.client.login(username='sam', password='sampass1234')
        self.client.get(reverse('home'))  # fija la org activa por defecto
        before = self.client.session.get('active_org_id')
        self.client.post(reverse('org-switch'), {'organization': self.foreign.id})
        self.assertEqual(self.client.session.get('active_org_id'), before)
        self.assertNotEqual(self.client.session.get('active_org_id'), self.foreign.id)


class OrgScopedBoardsTests(TestCase):
    @classmethod
    def setUpTestData(cls):
        cls.user = User.objects.create_user('teo', password='teopass1234')
        cls.org_a = Organization.objects.create(name='Org A')
        cls.org_b = Organization.objects.create(name='Org B')
        OrganizationMembership.objects.create(organization=cls.org_a, user=cls.user, role='admin')
        OrganizationMembership.objects.create(organization=cls.org_b, user=cls.user, role='admin')
        Board.objects.create(owner=cls.user, organization=cls.org_a, name='Tablero A')
        Board.objects.create(owner=cls.user, organization=cls.org_b, name='Tablero B')

    def test_home_lists_only_active_org_boards(self):
        self.client.login(username='teo', password='teopass1234')
        names = {b.name for b in self.client.get(reverse('home')).context['boards']}
        self.assertEqual(names, {'Tablero A'})  # Org A por defecto (orden alfabético)

        self.client.post(reverse('org-switch'), {'organization': self.org_b.id})
        names = {b.name for b in self.client.get(reverse('home')).context['boards']}
        self.assertEqual(names, {'Tablero B'})

    def test_create_stamps_active_org(self):
        self.client.login(username='teo', password='teopass1234')
        self.client.get(reverse('home'))  # org activa = Org A
        self.client.post(reverse('board-create'), {'name': 'Nuevo'})
        board = Board.objects.get(name='Nuevo')
        self.assertEqual(board.organization, self.org_a)


class ThemeRobustnessTests(TestCase):
    def test_save_rejects_invalid_hex(self):
        org = Organization.objects.create(name='Rob Co')
        with self.assertRaises(ValidationError):
            OrganizationTheme.objects.create(organization=org, brand_accent='nothex')

    def test_malformed_hex_in_db_does_not_crash_page(self):
        user = User.objects.create_user('rob', password='robpass1234')
        org = Organization.objects.create(name='Rob Co')
        OrganizationMembership.objects.create(organization=org, user=user, role='admin')
        theme = OrganizationTheme.objects.create(organization=org, brand_accent='#d6ff00')
        # Forzar un valor corrupto saltándose la validación de save() (update()).
        OrganizationTheme.objects.filter(pk=theme.pk).update(brand_accent='zzzzzz')
        self.client.login(username='rob', password='robpass1234')
        resp = self.client.get(reverse('home'))
        self.assertEqual(resp.status_code, 200)  # degrada con gracia, no 500


